						<div class="row">

							<div class="col-sm-12" data-nekoanim="fadeInRightBig" data-nekodelay="300">

<h3>Quelle est l’utilité de cette politique ?</h3>

<p><strong>Nous accordons une grande importance à la protection et la confidentialité de vos données personnelles qui représentent pour nous un gage de sérieux et de confiance.</strong></p>

<p>La politique de confidentialité des données témoigne précisément de <strong>notre volonté de faire respecter les règles applicables en matière de protection des données</strong> et, notamment, celles du Règlement général sur la protection des Données (« RGPD »).&nbsp;</p>

<p>En particulier, la politique de confidentialité vise à vous informer sur la manière et les raisons pour lesquelles nous traitons vos données dans le cadre des services que nous fournissons.</p>

<h3>À qui s’adresse cette politique ?</h3>

<p>La politique s’applique à <strong>vous</strong>, dès lors que vous êtes âgé d’au moins <strong>15 ans</strong>, quel que soit votre lieu de domiciliation, que vous soyez un <strong>client</strong>, un <strong>adhérent</strong>, un <strong>potentiel client</strong> (« prospect ») ou encore un <strong>simple visiteur</strong>.&nbsp;</p>

<p>Si vous avez <strong>moins de 15 ans</strong>, vous n’êtes pas autorisé à utiliser nos services sans le <strong>consentement préalable</strong> et <strong>explicite </strong>de l’un de vos parents qui doit être adressé par écrit à l’adresse rgpd@collegedesbernardins.fr. Si vous pensez que nous pourrions détenir des informations concernant un de vos enfants âgés de moins de 15 ans <strong>sans y avoir consenti</strong>, vous pouvez nous demander que nous les <strong>supprimions </strong>à l’adresse rgpd@collegedesbernardins.fr.</p>

<h3>Pourquoi traitons-nous vos données et sur quels fondements ?</h3>

<p>Dans le cadre des services proposés, nous sommes nécessairement amenés à traiter vos données personnelles pour les raisons et les fondements suivants :&nbsp;</p>

<ul>
	<li>Pour que vous puissiez <strong>bénéficier de nos services</strong> (ex : créer un compte en ligne, accéder à nos locaux, suivre nos formations, visiter des expositions, participer aux débats, etc.) et pour que nous puissions <strong>répondre à vos demandes</strong> (ex : demande d’information, réclamations, etc.) sur la base de nos conditions générales de vente, de nos conditions générales d’utilisation, et de notre intérêt légitime à vous fournir le meilleur service possible.&nbsp;</li>
	<li>Pour que vous puissiez effectuer des <strong>donations </strong>auprès de notre organisme sur la base de nos conditions générales d’utilisation et de notre intérêt légitime à traiter vos versements.&nbsp;</li>
	<li>Pour que vous restiez informés de nos <strong>dernières offres promotionnelles et événements</strong> par email, par voie téléphonique et postale, sur la base de notre intérêt légitime à vous fidéliser et sur la base de votre consentement, dans le cas où vous ne bénéficierez pas encore de nos services.&nbsp;</li>
	<li>Pour <strong>garantir et renforcer la sécurité et la qualité de nos services</strong> au quotidien (ex : statistiques, sécurité des données, etc.) sur la base des obligations légales pesant sur nous, de nos conditions générales de vente et de notre intérêt légitime à assurer le bon fonctionnement de nos services.&nbsp;</li>
	<li>Pour <strong>assurer votre sécurité</strong> au sein de nos locaux en utilisant des caméras de vidéoprotection sur la base de notre intérêt légitime.&nbsp;</li>
</ul>

<p>Nous nous engageons à ne traiter vos données que pour les <strong>raisons décrites précédemment</strong>.</p>

<h3>Quelles données traitons-nous et pour combien de temps ?</h3>

<p>Nous avons résumé les <strong>catégories de données personnelles</strong> que nous collectons directement <strong>auprès de vous</strong> ainsi que leurs <strong>durées respectives de conservation</strong>.&nbsp;</p>

<p>Si vous souhaitez obtenir encore plus de détails sur les durées de conservation, vous pouvez nous contacter à l’adresse rgpd@collegedesbernardins.fr.&nbsp;</p>

<ul>
	<li>Données d’<strong>identification personnelles, professionnelles</strong> et <strong>coordonnées </strong>(ex : nom, prénom, adresse email, adresse postale, etc.) conservées pendant toute la durée de la fourniture du service à laquelle s’ajoutent les délais de prescriptions légaux qui sont généralement de 5 ans.</li>
	<li>Données relatives à la <strong>religion </strong>et aux <strong>croyances </strong>conservées pendant toute la durée du service à laquelle s’ajoutent un délai de prescription de 5 ans. &nbsp;</li>
	<li>Données d’ordre <strong>économique </strong>et <strong>financier </strong>(ex : numéro de compte bancaire, code de vérification, etc.) conservées pendant la durée nécessaire à la transaction et à la gestion de la facturation et des paiements à laquelle s’ajoutent les délais de prescription légaux qui sont généralement de 5 ans à 10 ans.&nbsp;</li>
	<li>Données aux fins de <strong>prospection commerciale</strong> et de <strong>marketing </strong>(ex : adresse email, etc.) conservées pendant une durée maximum de 3 ans à compter du dernier contact que nous avons eu avec vous.&nbsp;</li>
	<li><strong>Images de vidéoprotection</strong> conservées pendant une durée de 1 mois maximum.&nbsp;</li>
	<li>Données de <strong>connexion </strong>(ex : logs, adresse IP, etc.) conservées pendant une durée de 1 an.&nbsp;</li>
</ul>

<p>Nous précisons qu'en aucun cas nous ne collectons de <strong>pièces d’identité</strong> vous concernant (ex : carte d’identité ou passeport, numéro de sécurité sociale ou permis de conduire).&nbsp;</p>

<p>À l’expiration des durées de conservation résumées précédemment, nous <strong>supprimons </strong>toutes vos données personnelles afin de garantir votre confidentialité pour les années futures. &nbsp;</p>

<p>La suppression de vos données personnelles est <strong>irréversible </strong>et nous ne pourrons plus vous les communiquer passé ce délai. Tout au plus, nous pouvons uniquement conserver des données anonymes à des fins de <strong>statistiques</strong>.&nbsp;</p>

<p>Veuillez également noter qu’en cas de <strong>contentieux</strong>, nous avons pour obligation de conserver <strong>l’intégralité</strong> des données vous concernant durant toute la durée de traitement du dossier même après l’expiration de leurs durées de conservation décrites précédemment.&nbsp;</p>

<h3>De quels droits disposez-vous pour contrôler l’usage de vos données ?</h3>

<p>La réglementation applicable en matière de protection des données vous octroie <strong>des droits spécifiques</strong> que vous pouvez exercer, <strong>à tout moment</strong> et <strong>gratuitement</strong>, afin de contrôler l’usage que nous faisons de vos données.&nbsp;</p>

<ul>
	<li>Droit d’<strong>accès </strong>et de <strong>copie </strong>de vos données personnelles dès lors que cette demande n’est pas en contradiction avec le secret des affaires, la confidentialité, ou encore le secret des correspondances.&nbsp;</li>
	<li>Droit de <strong>rectification </strong>de données personnelles qui seraient erronées, obsolètes ou incomplètes.&nbsp;</li>
	<li>Droit de vous <strong>opposer </strong>aux traitements de vos données personnelles mis en œuvre à des fins de prospection commerciale.&nbsp;</li>
	<li>Droit de demander <strong>l’effacement </strong>(“droit à l’oubli”) de vos données personnelles qui ne seraient pas essentielles au bon fonctionnement de nos services.&nbsp;</li>
	<li>Droit à la <strong>limitation </strong>de vos données personnelles qui permet de photographier l’utilisation de vos données en cas de contestation sur la légitimité d’un traitement.&nbsp;</li>
	<li>Droit à la <strong>portabilité </strong>de vos données qui vous permet de récupérer une partie de vos données personnelles afin de les stocker ou les transmettre facilement d’un système d’information à un autre.&nbsp;</li>
	<li>Droit de donner des <strong>directives </strong>sur le sort de vos données en cas de décès soit par votre intermédiaire, soit l’intermédiaire d’un tiers de confiance ou d’un ayant-droit.&nbsp;</li>
</ul>

<p>Pour qu’une demande soit <strong>prise en compte</strong>, il est impératif qu’elle soit effectuée directement par <strong>vous </strong>à l’adresse rgpd@collegedesbernardins.fr. Toute demande qui n’est pas effectuée de cette manière <strong>ne peut pas être traitée</strong>.&nbsp;</p>

<p>Les demandes ne peuvent pas émaner d’une autre personne que vous. Nous pouvons, uniquement dans ce cas précis, vous demander la communication d’une <strong>preuve d’identité</strong> en cas de doute sur l’identité du demandeur. &nbsp;</p>

<p>Nous répondrons à votre demande dans les <strong>meilleurs délais</strong>, dans la limite de <strong>deux mois</strong> à compter de sa réception, au cas où la demande serait techniquement complexe ou si nous recevons de nombreuses demandes au même moment. &nbsp;</p>

<p>Veuillez noter que nous pouvons toujours <strong>refuser </strong>de répondre à toute demande <strong>excessive </strong>ou <strong>infondée </strong>notamment au regard de son caractère <strong>répétitif</strong>.&nbsp;</p>

<h3>Qui peut avoir accès à vos données ?</h3>

<p>Nous ne communiquons vos données qu’aux personnes <strong>dûment autorisées</strong> à les utiliser pour mettre en œuvre nos services. Il peut notamment s’agir de notre personnel en charge de la mise en œuvre du service, de la comptabilité, de la communication ou même de la sécurité de nos locaux. &nbsp;</p>

<p>Nous pouvons également communiquer vos données <strong>aux autorités publiques</strong>, <strong>à des conseils</strong> et <strong>praticiens externes</strong>, ainsi qu’à des <strong>prestataires </strong>ou encore, éventuellement, à des <strong>partenaires</strong>.&nbsp;</p>

<h3>Comment protégeons-nous vos données ?</h3>

<p>Nous mettons en œuvre tous les <strong>moyens techniques</strong> et <strong>organisationnels requis</strong> pour garantir la <strong>sécurité </strong>de vos données au quotidien et, en particulier, pour lutter contre tout risque de destruction, perte, altération, ou divulgation de vos données qui ne serait pas autorisée.&nbsp;</p>

<p>En particulier, nous utilisons une technologie de <strong>chiffrement </strong>pour crypter vos données personnelles, nous disposons de serveurs de secours pour la <strong>sauvegarde </strong>de vos données personnelles et tout notre matériel informatique est évidemment équipé des derniers <strong>antivirus et firewall</strong> à jour.</p>

<h3>Vos données peuvent-elles être transférées en dehors de l’Union européenne ?</h3>

<p>Sauf si cela s’avère strictement nécessaire et de manière exceptionnelle, nous ne transférons jamais vos données en dehors de l’Union européenne et vos données sont toujours hébergées sur le <strong>sol européen</strong>. En outre, nous faisons notre maximum pour ne recruter que des prestataires hébergeant vos données au sein de l’Union européenne. &nbsp;</p>

<p>Dans le cas où nos prestataires seraient néanmoins amenés à transférer des données personnelles vous concernant en dehors de l’Union européenne, nous veillons scrupuleusement à ce qu’ils mettent en œuvre les <strong>garanties appropriées</strong> afin d’assurer la confidentialité et la protection de vos données.&nbsp;</p>

<h3>Qui pouvez-vous contacter pour obtenir plus d’informations ?</h3>

<p>Notre <strong>Délégué à la protection des données</strong> (« DPO ») est toujours à votre disposition pour vous expliquer plus en détail la manière dont nous traitons vos données et pour répondre à vos questions sur le sujet à l’adresse rgpd@collegedesbernardins.fr. &nbsp;</p>

<h3>Comment pouvez-vous contacter la CNIL ?</h3>

<p>Vous pouvez à tout moment contacter l’autorité de contrôle française en matière de protection des données (la «&nbsp;<strong>Commission nationale de l'informatique et des libertés&nbsp;</strong>» ou «&nbsp;<strong>CNIL&nbsp;</strong>») aux coordonnées suivantes : Service des plaintes de la CNIL, 3 place de Fontenoy – TSA 80751, 75334 Paris Cedex 07 ou par téléphone au 01.53.73.22.22.&nbsp;</p>

<h3>La politique peut-elle être modifiée ?</h3>

<p>Nous sommes susceptibles de <strong>modifier </strong>notre politique de confidentialité <strong>à tout moment</strong> pour l’adapter aux nouvelles <strong>exigences légales</strong> ainsi qu’aux <strong>nouveaux traitements</strong> que nous pourrions mettre en œuvre dans le futur. Vous serez évidemment informés de toute modification de cette politique.&nbsp;</p>


